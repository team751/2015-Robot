package org.team751.robot2015.utils.mecanum;

public class BarnMecanumOutput {
	public double	leftFrontSpeed;
	public double	rightFrontSpeed;
	public double	leftRearSpeed;
	public double	rightRearSpeed;

	public BarnMecanumOutput(double leftFrontSpeed, double rightFrontSpeed, double leftRearSpeed, double rightRearSpeed) {
		this.leftFrontSpeed = leftFrontSpeed;
		this.rightFrontSpeed = rightFrontSpeed;
		this.leftRearSpeed = leftRearSpeed;
		this.rightRearSpeed = rightRearSpeed;
	}
}
